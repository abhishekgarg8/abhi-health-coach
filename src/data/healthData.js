// ─── HELPER FUNCTIONS ──────────────────────────────────────────────────
export const avg = (arr) =>
  arr.length ? Math.round(arr.reduce((a, b) => a + b, 0) / arr.length) : 0;

export const computeStats = () => {
  const steps = stepsRaw.map((d) => d.steps);
  const cals = caloriesRaw.map((d) => d.cal);
  const dists = distanceRaw.map((d) => d.km);
  const rhrs = restingHRRaw.map((d) => d.rhr);
  const hrvs = hrvRaw.map((d) => d.hrv);
  const sleepTotals = sleepRaw.map((d) => d.total);
  const deepTotals = sleepRaw.map((d) => d.deep);

  return {
    stepsAvg: avg(steps),
    stepsMax: Math.max(...steps),
    stepsMin: Math.min(...steps),
    calAvg: avg(cals),
    calMax: Math.max(...cals),
    calMin: Math.min(...cals),
    distAvg: +(dists.reduce((a, b) => a + b, 0) / dists.length).toFixed(1),
    rhrAvg: avg(rhrs),
    hrvAvg: avg(hrvs),
    sleepAvg: +(sleepTotals.reduce((a, b) => a + b, 0) / sleepTotals.length).toFixed(1),
    deepAvg: +(deepTotals.reduce((a, b) => a + b, 0) / deepTotals.length).toFixed(1),
    daysOver10k: steps.filter((s) => s >= 10000).length,
    daysUnder5k: steps.filter((s) => s < 5000).length,
    totalDays: steps.length,
  };
};
